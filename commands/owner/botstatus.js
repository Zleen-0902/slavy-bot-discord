const { SlashCommandBuilder, ActivityType, MessageFlags } = require('discord.js');

module.exports = {
    data: new SlashCommandBuilder()
        .setName('botstatus')
        .setDescription('üî± Change the bot presence (Owner Only)')
        .addStringOption(option =>
            option.setName('type')
                .setDescription('Type of activity')
                .setRequired(true)
                .addChoices(
                    { name: 'Playing', value: '0' },
                    { name: 'Streaming', value: '1' },
                    { name: 'Listening', value: '2' },
                    { name: 'Watching', value: '3' },
                ))
        .addStringOption(option =>
            option.setName('content')
                .setDescription('The status message')
                .setRequired(true)),
    async execute(interaction) {
    await interaction.deferReply();
        // SECURITY: Replace with your Discord ID
        if (interaction.user.id !== process.env.OWNER_ID) {
            return interaction.editReply({ content: '‚ùå This command is restricted to the bot owner.', flags: [MessageFlags.Ephemeral] });
        }

        const type = parseInt(interaction.options.getString('type'));
        const content = interaction.options.getString('content');

        try {
            await interaction.client.user.setPresence({
                activities: [{ name: content, type: type }],
                status: 'online',
            });

            await interaction.editReply({ content: `‚úÖ Status updated to: **${content}**`, flags: [MessageFlags.Ephemeral] });
        } catch (error) {
            console.error(error);
            await interaction.editReply({ content: '‚ùå Failed to update status.', flags: [MessageFlags.Ephemeral] });
        }
    },
};